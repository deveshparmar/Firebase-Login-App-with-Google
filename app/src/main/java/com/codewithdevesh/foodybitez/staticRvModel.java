package com.codewithdevesh.foodybitez;

public class staticRvModel {
      private int image;
      private String text;

    public staticRvModel(int image, String text) {
        this.image = image;
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public String getText() {
        return text;
    }

}
